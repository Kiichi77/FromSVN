package cn.com.kxcomm.contractmanage.vo;

/**
 * 
 * 功能描述:审核询价单vo
 * 
 * @author chenliang 新增日期：2013-3-4
 * @since ContractManage
 */
public class CheckinquiryOrderVo {

	private String id;
	private String cauditTime; // 审核时间
	private String status; // 状态
	private String totalPrice; // 成交总价
	private String catalogPrice; // 目录合价总价
	private String purchasePrice; // 采购总价
	private String orderNo; // 销售订单号
	private String inquiryer; // 毛利率
	
	private String configModelName;//机型配置名称
	private String configModelId;//机型配置id
	private String quantity; //数量
	private String suppliersQuotationUrl; //供应商报价表下载地址
	
	public String getSuppliersQuotationUrl() {
		return suppliersQuotationUrl;
	}

	public void setSuppliersQuotationUrl(String suppliersQuotationUrl) {
		this.suppliersQuotationUrl = suppliersQuotationUrl;
	}

	public String getConfigModelName() {
		return configModelName;
	}

	public void setConfigModelName(String configModelName) {
		this.configModelName = configModelName;
	}

	public String getConfigModelId() {
		return configModelId;
	}

	public void setConfigModelId(String configModelId) {
		this.configModelId = configModelId;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCauditTime() {
		return cauditTime;
	}

	public void setCauditTime(String cauditTime) {
		this.cauditTime = cauditTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}

	public String getCatalogPrice() {
		return catalogPrice;
	}

	public void setCatalogPrice(String catalogPrice) {
		this.catalogPrice = catalogPrice;
	}

	public String getPurchasePrice() {
		return purchasePrice;
	}

	public void setPurchasePrice(String purchasePrice) {
		this.purchasePrice = purchasePrice;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getInquiryer() {
		return inquiryer;
	}

	public void setInquiryer(String inquiryer) {
		this.inquiryer = inquiryer;
	}

	@Override
	public String toString() {
		return "CheckinquiryOrderVo [id=" + id + ", cauditTime=" + cauditTime
				+ ", status=" + status + ", totalPrice=" + totalPrice
				+ ", catalogPrice=" + catalogPrice + ", purchasePrice="
				+ purchasePrice + ", orderNo=" + orderNo + ", inquiryer="
				+ inquiryer + "]";
	}

}

package cn.com.kxcomm.contractmanage.vo;

public class PurchaseInvoiceVo {
  private String id;
  private String suppliers;
  private String purchaseCompany;
  private String purchaseOrderContractFile;
  private String purchaseAccount;
  private String purchaseBankOpen;
  private String taxpayerNo;
  private String dateInvoice;
  private String invoiceNo;
  private String amount;
  private String pathName;
  private String createtime;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getSuppliers() {
	return suppliers;
}
public void setSuppliers(String suppliers) {
	this.suppliers = suppliers;
}
public String getPurchaseCompany() {
	return purchaseCompany;
}
public void setPurchaseCompany(String purchaseCompany) {
	this.purchaseCompany = purchaseCompany;
}
public String getPurchaseOrderContractFile() {
	return purchaseOrderContractFile;
}
public void setPurchaseOrderContractFile(String purchaseOrderContractFile) {
	this.purchaseOrderContractFile = purchaseOrderContractFile;
}

public String getPurchaseAccount() {
	return purchaseAccount;
}
public void setPurchaseAccount(String purchaseAccount) {
	this.purchaseAccount = purchaseAccount;
}
public String getPurchaseBankOpen() {
	return purchaseBankOpen;
}
public void setPurchaseBankOpen(String purchaseBankOpen) {
	this.purchaseBankOpen = purchaseBankOpen;
}
public String getTaxpayerNo() {
	return taxpayerNo;
}
public void setTaxpayerNo(String taxpayerNo) {
	this.taxpayerNo = taxpayerNo;
}
public String getDateInvoice() {
	return dateInvoice;
}
public void setDateInvoice(String dateInvoice) {
	this.dateInvoice = dateInvoice;
}
public String getInvoiceNo() {
	return invoiceNo;
}
public void setInvoiceNo(String invoiceNo) {
	this.invoiceNo = invoiceNo;
}
public String getAmount() {
	return amount;
}
public void setAmount(String amount) {
	this.amount = amount;
}
public String getPathName() {
	return pathName;
}
public void setPathName(String pathName) {
	this.pathName = pathName;
}
public String getCreatetime() {
	return createtime;
}
public void setCreatetime(String createtime) {
	this.createtime = createtime;
}
@Override
public String toString() {
	return "PurchaseInvoiceVo [id=" + id + ", suppliers=" + suppliers
			+ ", purchaseCompany=" + purchaseCompany
			+ ", purchaseOrderContractFile=" + purchaseOrderContractFile
			+ ", purchaseAccount=" + purchaseAccount + ", purchaseBankOpen="
			+ purchaseBankOpen + ", taxpayerNo=" + taxpayerNo
			+ ", dateInvoice=" + dateInvoice + ", invoiceNo=" + invoiceNo
			+ ", amount=" + amount + ", pathName=" + pathName + ", createtime="
			+ createtime + "]";
}

  
}

package cn.com.kxcomm.contractmanage.vo;

/**
 * 
 * 功能描述:全通用单元的vo
 * 
 * @author chenliang 新增日期：2013-3-4
 * @since ContractManage
 */
public class CurrentUnitVo {

	private String id;
	private String configModelDataId; // 机型配置数据id
	private String quantity; // 总数量
	private String unitPrice; // 单价
	private String typeName; // 类型名称
	private String categoryName; // 总类型名称
	private String productDesc; // 产品描述
	private String productOrderNo; // 产品编号
	private String ordered; // 已下单数
	private String totalPrice;//总价
	
	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getConfigModelDataId() {
		return configModelDataId;
	}

	public void setConfigModelDataId(String configModelDataId) {
		this.configModelDataId = configModelDataId;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getUnitPrice() {
		return unitPrice;
	}

	public void setUnitPrice(String unitPrice) {
		this.unitPrice = unitPrice;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public String getProductDesc() {
		return productDesc;
	}

	public void setProductDesc(String productDesc) {
		this.productDesc = productDesc;
	}

	public String getProductOrderNo() {
		return productOrderNo;
	}

	public void setProductOrderNo(String productOrderNo) {
		this.productOrderNo = productOrderNo;
	}

	public String getOrdered() {
		return ordered;
	}

	public void setOrdered(String ordered) {
		this.ordered = ordered;
	}

	@Override
	public String toString() {
		return "CurrentUnitVo [quantity=" + quantity + ", unitPrice="
				+ unitPrice + ", typeName=" + typeName + ", categoryName="
				+ categoryName + ", productDesc=" + productDesc
				+ ", productOrderNo=" + productOrderNo + ", ordered=" + ordered
				+ "]";
	}

}
